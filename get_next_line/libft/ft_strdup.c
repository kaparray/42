#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*result;
	size_t	len;

	len = ft_strlen(src) + 1;
	if (!(result = malloc(sizeof(*src) * ft_strlen(src) + 1)))
		return (NULL);
	ft_memcpy(result, src, len);
	return (result);
}
