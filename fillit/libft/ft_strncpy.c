#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t index;

	index = -1;
	while (++index < len)
		if (*(src + index))
			*(dst + index) = *(src + index);
		else
			while (index < len)
				*(dst + index++) = '\0';
	return (dst);
}
